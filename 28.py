import numpy as np

# Введення массиву
input_massive = np.array([int(input("Введіть елемент масиву: ")) for i in range(int(input("Введіть довжину масиву: ")))])
# Виведення масиву та кількості парних елементів
print("Ваш масив: ", input_massive)
print(f"Кількість парних елементів: {sum(((i % 2) == 0) for i in input_massive)}")